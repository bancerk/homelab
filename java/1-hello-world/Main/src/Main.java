public class Main {
    public static void main(String[] args) {
        
        // This is a comment
        // start coding here
        
        System.out.print("Hello, World!");
        System.out.print("I love pizza");

        System.out.print("Hello, World!\t");
        System.out.print("\nI love pizza");

        // \n - new line
        // \t - tab

        System.out.println("Hello, World!");
        System.out.println("I love pizza");;

        // println - print to new line
        // print - print to same line

        /* this is a series of comments
         * that span multiple lines
         * and are ignored by the compiler
         */

         // just type 'sysout' and press tab for IDE to auto complete to System.out.println();
    }
}